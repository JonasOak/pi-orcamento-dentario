    private void aoClicarExcluir(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aoClicarExcluir
        int resp = JOptionPane.showConfirmDialog(null,"Deseja Excluir o Registro?","Exclus√£o de Registro",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
          
        if (resp == 0) {
            this.operacao = Operacao.EXCLUIR;
            tfOperacao.setText(this.operacao.name());
            this.aoClicarConfirmar(evt);
            btAlterar.setEnabled(false);
            btExcluir.setEnabled(false);
        }
        else {
            tfPesquisar.requestFocus();
        }
    }//GEN-LAST:event_aoClicarExcluir

    private void aoClicarConfirmar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aoClicarConfirmar
       switch (operacao) {
            case INSERIR:
                try {
                    ClienteController clienteController = DaoFactory.criarClienteController();
                    Cliente cliente = new Cliente(); 
                    cliente.setDocumento(ftDocumento.getText());
                    cliente.setNomeCliente(tfNome.getText());
                    cliente.setEndereco(tfEndereco.getText());
                    cliente.setUf(tfUF.getText());
                    cliente.setTelefone(ftTel.getText());
                    cliente.setEmail(tfEmail.getText());
                   
                    clienteController.inserir(cliente);
                    JOptionPane.showMessageDialog(null, "Opera√ß√£o Conclu√≠da com Sucesso", "Inser√ß√£o", JOptionPane.INFORMATION_MESSAGE);
                    this.operacao = Operacao.INATIVO;
                    tfOperacao.setText(this.operacao.name());
                    btConfirmar.setEnabled(false);
                }
                catch (Exception e) {
                    JOptionPane.showMessageDialog(null,"Houve um erro na inser√ß√£o","Inser√ß√£o",JOptionPane.ERROR_MESSAGE);
                }
             break;
             
            case ALTERAR:
                try {
                    ClienteController clienteController = DaoFactory.criarClienteController();
                    Cliente cliente = new Cliente();
                    
                    cliente.setIdCliente(Integer.parseInt(tfCodigo.getText()));
                    cliente.setDocumento(ftDocumento.getText());
                    cliente.setNomeCliente(tfNome.getText());
                    cliente.setEndereco(tfEndereco.getText());
                    cliente.setUf(tfUF.getText());
                    cliente.setTelefone(ftTel.getText());
                    cliente.setEmail(tfEmail.getText());
                    
                    clienteController.atualizar(cliente);  
                    JOptionPane.showMessageDialog(null,"Opera√ß√£o Conclu√≠da com Sucesso","Altera√ß√£o",JOptionPane.INFORMATION_MESSAGE);
                    this.operacao = Operacao.INATIVO;
                    tfOperacao.setText(this.operacao.name());
                    btConfirmar.setEnabled(false);
                    
                }
                catch (Exception e) {
                    JOptionPane.showMessageDialog(null,"Houve um erro na altera√ß√£o","Altera√ß√£o",JOptionPane.ERROR_MESSAGE);
                }
            break;
            
            case EXCLUIR:
                try {
                    ClienteController clienteController = DaoFactory.criarClienteController();
                    Cliente cliente = new Cliente();
                    
                    cliente.setIdCliente(Integer.parseInt(tfCodigo.getText()));
                    cliente.setDocumento(ftDocumento.getText());
                    cliente.setNomeCliente(tfNome.getText());
                    cliente.setEndereco(tfEndereco.getText());
                    cliente.setUf(tfUF.getText());
                    cliente.setTelefone(ftTel.getText());
                    cliente.setEmail(tfEmail.getText());
                    
                    clienteController.deletarPorId(cliente.getIdCliente());
                    JOptionPane.showMessageDialog(null,"Opera√ß√£o Conclu√≠da com Sucesso","Exclus√£o",JOptionPane.INFORMATION_MESSAGE);
                    this.operacao = Operacao.INATIVO;
                    tfOperacao.setText(this.operacao.name());
                    btConfirmar.setEnabled(false);
                   
                    tfCodigo.setText("");
                    ftDocumento.setText("");
                    tfNome.setText("");
                    ftTel.setText("");
                    tfUF.setText("");
                    tfEndereco.setText("");
                    tfEmail.setText("");
                    tfPesquisar.setText("");
                    tfPesquisar.requestFocus();
                }
                catch (Exception e) {
                    JOptionPane.showMessageDialog(null,"Houve um erro na exclus√£o","Exclus√£o",JOptionPane.ERROR_MESSAGE);
                }
            break;
        } 
    }//GEN-LAST:event_aoClicarConfirmar

